include_once(_PS_MODULE_DIR_.'eter_faq/classes/faq.php');
class eter_enviayatrackingModuleFrontController extends ModuleFrontController
{
    /**
     * Define globlal variables
     */
    public $bootstrap = true;
    public $_faq =  null;
    public $_template = 'module:eter_faq/views/templates/front/question_list.tpl';
    
    public function init() {
        parent::init();
    }
    /**
     * This method start with the execution
     */
	public function initContent() 
	{
        parent::initContent();
		$this->_faq = new Faq();
		$questions = $this->_faq->getData();
        $questionOrder = [];
        $questionMain = [];
        $current = null;
        foreach ($questions as &$question) {
            if ($question['active']) {
                $question['url'] = $this->context->link->getModuleLink('eter_faq','question',['url_rewrite' => $question['url_rewrite'] ]);
                $question['current'] = false;
                if (Tools::getValue('url_rewrite') == $question['url_rewrite']) {
                    $question['current'] = true;
                    $current = $question;
                    $questionMain[] = $question;
                } else {
                    $questionOrder[] = $question;
                }
            }
        }
        $questions = array_merge($questionMain,$questionOrder);
        $this->context->smarty->assign([
        	'question_list' => $questions,
        	'url' => $this->context->link->getModuleLink('eter_faq','url'),
            'current' => $current,
            'url_faq' => $this->context->link->getModuleLink('eter_faq','faq'),
            'url_rewrite' => Tools::getValue('url_rewrite')?Tools::getValue('url_rewrite'):'0'
        ]);
        $this->setTemplate($this->_template);
	}
 
}